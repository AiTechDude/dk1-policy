def get_makefile_targets(repo: Path) -> tuple[bool, list[str]]:
    mk = repo / "Makefile"
    if not mk.is_file():
        return False, []
    import re
    targets = []
    for line in mk.read_text().splitlines():
        m = re.match(r"^([a-zA-Z][a-zA-Z0-9_\-]+):\s*", line)
        if m:
            target = m.group(1)
            # Filter out generic non-target patterns
            if target not in ("PHONY", "all") or target not in targets:
                if target not in targets:
                    targets.append(target)
    return True, targets


def write_if_absent(path: Path, content: str) -> bool:
    if path.exists():
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content)
    return True


def merge_agents_md(repo: Path, project_name: str, project_state_path: str) -> str:
    """Prepend handoff template to AGENTS.md, preserving any existing content."""
    agents = repo / "AGENTS.md"
    state = existing_agents_state(agents)
    prepend = agents_prepend_block(project_name, project_state_path)
    if state == "absent":
        agents.write_text(prepend + "\n## graphify\n\n(Run `graphify codex install` to populate.)\n")
        return "created"
    if state == "already-merged":
        return "skipped (already merged)"
    existing = agents.read_text().rstrip() + "\n"
    agents.write_text(prepend + "\n" + existing)
    return f"prepended ({state})"


def upsert_agent_yaml(repo: Path, repo_name: str, project_name: str) -> str:
    yaml_path = repo / "dk1_agent.yaml"
    if not yaml_path.exists():
        yaml_path.write_text(minimal_agent_yaml(repo_name, project_name))
        return "created minimal yaml"
    text = yaml_path.read_text()
    if "agent_memory:" in text:
        return "skipped (agent_memory: already present)"
    yaml_path.write_text(text.rstrip() + "\n" + agent_memory_yaml_block())
    return "appended agent_memory block"


def main() -> int:
    if len(sys.argv) < 2:
        print("Usage: dk1-agent-memory-bootstrap.sh <repo_path> [project_display_name]", file=sys.stderr)
        return 2
    repo = Path(sys.argv[1]).expanduser().resolve()
    if not repo.is_dir():
        print(f"Not a directory: {repo}", file=sys.stderr)
        return 1
    repo_name = repo.name
    project_name = sys.argv[2] if len(sys.argv) >= 3 else repo_name

    am_dir = repo / "docs" / "agent-memory"
    am_dir.mkdir(parents=True, exist_ok=True)

    actions = []

    # Detect existing PROJECT_STATE.md location
    existing_ps = detect_project_state(repo)
    if existing_ps and existing_ps.parent.name != "agent-memory":
        # Existing PROJECT_STATE.md at root or docs/ — point read order there
        try:
            ps_rel = existing_ps.relative_to(repo)
        except ValueError:
            ps_rel = existing_ps
        ps_path_for_agents = str(ps_rel)
        actions.append(f"PROJECT_STATE.md: kept existing at {ps_path_for_agents}")
    else:
        # Either no existing or already in docs/agent-memory/
        ps_target = am_dir / "PROJECT_STATE.md"
        if write_if_absent(ps_target, project_state_template(project_name)):
            actions.append(f"PROJECT_STATE.md: created at docs/agent-memory/PROJECT_STATE.md")
        else:
            actions.append(f"PROJECT_STATE.md: kept existing at docs/agent-memory/PROJECT_STATE.md")
        ps_path_for_agents = "docs/agent-memory/PROJECT_STATE.md"

    # DECISIONS.md
    decisions_path = am_dir / "DECISIONS.md"
    if write_if_absent(decisions_path, decisions_template()):
        actions.append("DECISIONS.md: created")
    else:
        actions.append("DECISIONS.md: kept existing")

    # RUNBOOK.md
    runbook_path = am_dir / "RUNBOOK.md"
    has_mk, targets = get_makefile_targets(repo)
    if write_if_absent(runbook_path, runbook_template(has_mk, targets)):
        actions.append(f"RUNBOOK.md: created ({len(targets)} Makefile targets enumerated)" if has_mk else "RUNBOOK.md: created")
    else:
        actions.append("RUNBOOK.md: kept existing")

    # HANDOFF_LOG.md
    handoff_path = am_dir / "HANDOFF_LOG.md"
    if write_if_absent(handoff_path, handoff_log_initial(project_name)):
        actions.append("HANDOFF_LOG.md: created with initial entry")
    else:
        actions.append("HANDOFF_LOG.md: kept existing")

    # GRAPHIFY_NOTES.md
    metrics = graphify_metrics(repo)
    notes_path = am_dir / "GRAPHIFY_NOTES.md"
    if write_if_absent(notes_path, graphify_notes_template(metrics)):
        if metrics.get("present"):
            actions.append(f"GRAPHIFY_NOTES.md: created (auto-populated {metrics.get('nodes', '?')} nodes)")
        else:
            actions.append("GRAPHIFY_NOTES.md: created (no graphify-out found)")
    else:
        actions.append("GRAPHIFY_NOTES.md: kept existing")

    # SECURITY_NOTES.md (thin vs full based on existing SECURITY.md)
    sec_existing = detect_security(repo)
    sec_notes_path = am_dir / "SECURITY_NOTES.md"
    if sec_existing:
        if write_if_absent(sec_notes_path, security_notes_thin(project_name)):
            actions.append(f"SECURITY_NOTES.md: created (thin; cross-refs {sec_existing.relative_to(repo)})")
        else:
            actions.append("SECURITY_NOTES.md: kept existing")
    else:
        if write_if_absent(sec_notes_path, security_notes_full(project_name)):
            actions.append("SECURITY_NOTES.md: created (full template, no root SECURITY.md)")
        else:
            actions.append("SECURITY_NOTES.md: kept existing")

    # AGENTS.md merge
    agents_result = merge_agents_md(repo, project_name, ps_path_for_agents)
    actions.append(f"AGENTS.md: {agents_result}")

    # dk1_agent.yaml upsert
    yaml_result = upsert_agent_yaml(repo, repo_name, project_name)
    actions.append(f"dk1_agent.yaml: {yaml_result}")

    # Report
    print(f"DK1 agent memory bootstrap — {project_name}")
    print(f"  repo: {repo}")
    print(f"  agent-memory dir: {am_dir}")
    for a in actions:
        print(f"  • {a}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
