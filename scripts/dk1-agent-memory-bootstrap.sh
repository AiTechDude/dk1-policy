def detect_project_state(repo: Path) -> Path | None:
    """Return path to existing PROJECT_STATE.md if any, else None."""
    candidates = [
        repo / "PROJECT_STATE.md",
        repo / "docs" / "PROJECT_STATE.md",
        repo / "docs" / "agent-memory" / "PROJECT_STATE.md",
    ]
    for p in candidates:
        if p.is_file():
            return p
    return None


def detect_security(repo: Path) -> Path | None:
    """Return path to existing SECURITY.md if any."""
    for p in (repo / "SECURITY.md", repo / "docs" / "SECURITY.md"):
        if p.is_file():
            return p
    return None


def existing_agents_state(p: Path) -> str:
    """Classify AGENTS.md state: 'absent', 'stub', 'substantive'."""
    if not p.is_file():
        return "absent"
    txt = p.read_text()
    if "Mandatory Read Order" in txt:
        return "already-merged"
    if len(txt.splitlines()) <= 15 and "## graphify" in txt:
        return "stub"
    return "substantive"


def graphify_metrics(repo: Path) -> dict:
    """Extract last-run mtime + node/edge/community counts from GRAPH_REPORT.md."""
    report = repo / "graphify-out" / "GRAPH_REPORT.md"
    out = {"present": False}
    if not report.is_file():
        return out
    out["present"] = True
    out["mtime"] = report.stat().st_mtime
    try:
        head = report.read_text()[:4000]
        # Look for pattern like "- 3490 nodes · 7610 edges · 185 communities"
        import re
        m = re.search(r"(\d+)\s*nodes.*?(\d+)\s*edges.*?(\d+)\s*communities", head)
        if m:
            out["nodes"] = int(m.group(1))
            out["edges"] = int(m.group(2))
            out["communities"] = int(m.group(3))
        # Look for "Built from commit: `<sha>`"
        cm = re.search(r"Built from commit:\s*`([0-9a-f]+)`", head)
        if cm:
            out["commit"] = cm.group(1)
    except Exception:
        pass
    return out


